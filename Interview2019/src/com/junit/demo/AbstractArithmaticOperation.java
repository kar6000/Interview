package com.junit.demo;

abstract class AbstractArithmaticOperation {
		
	public static int intAdd(int x,int y) {
		int add =x+y;
		return add;
	}
	public static int intSub(int x,int y) {
		int sub =x-y;
		return sub;
	}
	
	public static int intMul(int x,int y) {
		int mul =x*y;
		return mul;
	}		
}
